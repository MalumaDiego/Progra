package mypackage1;

public class ClaseDep 
{
  String codigo;
  String nombre;
  String descr;
  String precio;

  public ClaseDep()
  {
  }

  public String getCodigo()
  {
    return codigo;
  }

  public void setCodigo(String newCodigo)
  {
    codigo = newCodigo;
  }

  public String getNombre()
  {
    return nombre;
  }

  public void setNombre(String newNombre)
  {
    nombre = newNombre;
  }

  public String getDescr()
  {
    return descr;
  }

  public void setDescr(String newDescr)
  {
    descr = newDescr;
  }

  public String getPrecio()
  {
    return precio;
  }

  public void setPrecio(String newPrecio)
  {
    precio = newPrecio;
  }
}